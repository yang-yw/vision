ORG 30H
CLC1 EQU 200
CLC2 EQU 125
MOV R2,#7
MOV A,#0FEH
MOV P2,A
DOWN:   RL A
  ACALL DELAY
  MOV P2,A
  DJNZ R2,DOWN
  MOV R2,#7
UP:  RR A
  ACALL DELAY
  MOV P2,A
  DJNZ R2,UP
  MOV R2,#7
  SJMP DOWN
  
  
DELAY:  MOV R7,CLC2
DEL1: MOV R6,CLC1
  DJNZ R6,$
  DJNZ R7,DEL1
  RET
END